// in for loop we know how many times loop to be executed
// in while loop we enter boolean means run the code untill that condition is true 
// O(n)

package com.test;

public class whileloopexample {
;
	public static void main (String[] args) {
		int i = 5;
		while (i>0) {
			System.out.println(i);
			i = i-1;
		}
	}

}
